async function main() {
    const [deployer] = await ethers.getSigners();
  
    console.log("Deploying contracts with the account:", deployer.address);
  
    console.log("Account balance:", (await deployer.getBalance()).toString());
  
    const Aurei = await ethers.getContractFactory("MutantAureliusAurei");
    const aurei = await Aurei.deploy();
  
    console.log("Aurei address:", aurei.address);
  }
  
  main()
    .then(() => process.exit(0))
    .catch((error) => {
      console.error(error);
      process.exit(1);
    });